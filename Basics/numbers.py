# working with numbers
# addition
print(10 + 3)

# subtraction
print(10 - 3)

# multiplication
print(10 * 3)

# to the power(left to the power of right)
print(10 ** 3)

# division
print(10 / 3)

# division without remainder
print(10 // 3)

# AUGMENTED ASSIGNMENT OPERATOR
x = 10
x = x + 30
print(x)

# using augmented operators
# addition
x += 3
print(x)

# subtraction
x -= 3
print(x)

# multiplication
x *= 3
print(x)

# to the power of
x **= 3
print(x)

# division
x /= 3
print(x)

# division without whole number
x //= 3
print(x)
