first = "python"
last = "programming"

# concatenating
print(first + " " + last)

# formating text
full_text = f"{first} {last}"
print(full_text)

# mathematical operation in formatted string
addition = f"{3 + 9}"
print(addition)

subtraction = f"{3 - 9}"
print(subtraction)

multiply = f"{3 * 9}"
print(multiply)

division = f"{3 / 9}"
print(division)
