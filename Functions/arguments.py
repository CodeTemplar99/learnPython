# an argument is an input passed when you call a function
# example
def greet(user):
    print(f"hello {user}")


greet("michael")
# this string "michael" is this argument
# this is the value to  the user variable(parameter) passed when defining this function
# the difference between argument and parameter is:
# parameters are defined when defining the function
# arguments are passed when calling the function
# NOTE
# the number of arguments must equal the number of parameters
