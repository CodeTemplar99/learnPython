for x in range(4):
    for y in range(2):
        print(f"(x:{x} , y:{y} )")

# nested loops are loops that contain another loop in it, the outermost loop is called the parent loop while the inner is called the child (or children)

# in the above code the x loop, the outer loop is run 4 times but during it's runtime the y loop, the inner loop is meant to run twice on every single iteration of the parent loop
