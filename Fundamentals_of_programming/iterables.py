# iterables are items that can be looped over
# in python: strings, range, list are iterable

# strings
for x in "python programming":
    print(x)
print("\n")


# range
for x in range(8):
    print(x)
print("\n")


# strings
for x in [5, 4, 3, 2, 1]:
    print(x)


# in every iteration of an iterable x will have a different value
