# comparison operators are used to compare objects in python programming
# greater than
print(2 > 4)

# less than
print(2 < 4)

# equality
print(2 == 4)

# assignment
x = 4
print(x)
# comparison in string

# not equal
print("hello" == "HELLO")

# equal
print("hello" == "hello")

# ord
print("b" > "B")
print(ord("b"))
print(ord("B"))
